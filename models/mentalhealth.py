def check_missing_values(df):
    """Check and report missing values"""
    missing = df.isnull().sum()
    missing = missing[missing > 0]
    if len(missing) > 0:
        print("\n⚠️ Missing values found:")
        for col, count in missing.items():
            print(f"  {col}: {count} ({count/len(df)*100:.1f}%)")
        return True
    else:
        print("✅ No missing values found")
        return False

def handle_missing_values(df):
    """Handle missing values based on column type"""
    for col in df.columns:
        if df[col].isnull().any():
            if df[col].dtype == 'object':
                df[col].fillna(df[col].mode()[0], inplace=True)
                print(f"  Filled {col} with mode: {df[col].mode()[0]}")
            else:
                df[col].fillna(df[col].median(), inplace=True)
                print(f"  Filled {col} with median: {df[col].median()}")
    return df

def check_class_balance(y):
    """Check if dataset is imbalanced"""
    balance = y.value_counts(normalize=True)
    min_class = balance.min()
    print(f"\nClass distribution:")
    print(balance)

    if min_class < 0.2:
        print(f"⚠️ Imbalanced dataset detected (minority class: {min_class:.1%})")
        return True
    else:
        print("✅ Dataset is balanced")
        return False

