class DepressionPredictor:
    def __init__(self):
        """Load all saved models and encoders"""
        print("Loading model and encoders...")

        # Reload config to ensure we get the latest definitions
        importlib.reload(config)
        # Explicitly define feature lists within this class
        # to ensure the latest definitions are used, overriding any cached imports.
        # This mirrors the approach in 02_feature_engineering.py which successfully handled config updates.
        self.NUMERICAL_FEATURES = [
            'age', 'daily_social_media_hours', 'sleep_hours',
            'screen_time_before_sleep', 'academic_performance',
            'physical_activity'
        ]

        self.CATEGORICAL_FEATURES = [
            'gender', 'platform_usage', 'digital_wellbeing_flag',
            'social_interaction_level', 'sleep_quality'
        ]

        self.model = load_object('depression_model.pkl')
        self.scaler = load_object('scaler.pkl')
        self.feature_names = load_object('feature_names.pkl')

        # Load encoders for categorical features
        self.encoders = {}
        for cat_col in self.CATEGORICAL_FEATURES:
            self.encoders[cat_col] = load_object(f'{cat_col}_encoder.pkl')

        print("✅ Ready to make predictions")

    def preprocess_input(self, data_dict):
        """
        Convert raw input dictionary to model-ready format

        Expected format:
        {
            'age': 25,
            'gender': 'Male',
            'daily_social_media_hours': 6,
            'platform_usage': 'Instagram',
            'sleep_hours': 5,
            'screen_time_before_sleep': 2,
            'academic_performance': 3,
            'physical_activity': 2,
            'social_interaction_level': 2,
            'sleep_quality': 3,
            'digital_wellbeing_flag': 'No'
        }
        """
        # Create dataframe
        df = pd.DataFrame([data_dict])

        # Encode categorical features
        for cat_col in self.CATEGORICAL_FEATURES:
            encoder = self.encoders[cat_col]
            input_value = str(df[cat_col].iloc[0])
            # Conditionally convert to lowercase based on how the encoder was trained
            if cat_col in ['gender', 'social_interaction_level']:
                input_value = input_value.lower()

            encoded_value = encoder.transform([input_value])[0]
            df[f'{cat_col}_encoded'] = encoded_value

        # Select features in correct order
        feature_values = []
        for feature in self.feature_names:
            if feature in self.NUMERICAL_FEATURES:
                feature_values.append(df[feature].iloc[0])
            else:  # Encoded feature
                feature_values.append(df[feature].iloc[0])

        # Scale numerical features
        X = np.array(feature_values).reshape(1, -1)
        numerical_indices = [i for i, f in enumerate(self.feature_names)
                            if f in self.NUMERICAL_FEATURES]

        # Ensure we're only scaling the numerical parts of X
        # X is a 2D array, so slicing needs to be careful
        numerical_data_to_scale = X[:, numerical_indices]
        X[:, numerical_indices] = self.scaler.transform(numerical_data_to_scale)

        return X

    def predict(self, data_dict):
        """Make prediction for a single person"""
        X = self.preprocess_input(data_dict)

        # Get prediction
        prediction = self.model.predict(X)[0]
        probability = self.model.predict_proba(X)[0][1]

        return {
            'depression': bool(prediction),
            'probability': float(probability),
            'risk_level': self._get_risk_level(probability)
        }

    def predict_batch(self, data_list):
        """Make predictions for multiple people"""
        results = []
        for data in data_list:
            results.append(self.predict(data))
        return results

    def _get_risk_level(self, probability):
        """Convert probability to risk level"""
        if probability >= 0.7:
            return "High Risk"
        elif probability >= 0.4:
            return "Moderate Risk"
        else:
            return "Low Risk"

