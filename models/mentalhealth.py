import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import roc_auc_score
from config import TEST_SIZE, RANDOM_STATE, N_ESTIMATORS, MAX_DEPTH, TARGET_COLUMN
from utils import save_object

def main():
    print("="*60)
    print("STEP 3: TRAINING DEPRESSION MODEL")
    print("="*60)

    # Load processed data
    df = pd.read_csv('data_processed.csv')

    # Separate features and target
    # Corrected: Use TARGET_COLUMN as it was not encoded into 'depression_label_encoded'
    X = df.drop(TARGET_COLUMN, axis=1)
    y = df[TARGET_COLUMN]

    print(f"Features: {X.columns.tolist()}")
    print(f"Target distribution:\n{y.value_counts(normalize=True)}")

    # Split data
    X_train, X_test, y_train, y_test = train_test_split(
        X, y, test_size=TEST_SIZE, random_state=RANDOM_STATE, stratify=y
    )

    print(f"\nTraining set: {len(X_train)} samples")
    print(f"Test set: {len(X_test)} samples")
    print(f"Training depression rate: {y_train.mean():.1%}")
    print(f"Test depression rate: {y_test.mean():.1%}")

    # Train model
    print("\nTraining Random Forest classifier...")
    model = RandomForestClassifier(
        n_estimators=N_ESTIMATORS,
        max_depth=MAX_DEPTH,
        class_weight='balanced',
        random_state=RANDOM_STATE,
        n_jobs=-1
    )

    model.fit(X_train, y_train)
    print("✅ Training complete")

    # Quick evaluation on test set
    y_pred_proba = model.predict_proba(X_test)[:, 1]
    auc = roc_auc_score(y_test, y_pred_proba)
    print(f"\n📊 Test ROC-AUC: {auc:.3f}")

    # Save model
    save_object(model, 'depression_model.pkl')

    # Save feature names for later use
    feature_names = X.columns.tolist()
    save_object(feature_names, 'feature_names.pkl')

    # Feature importance
    importance_df = pd.DataFrame({
        'feature': feature_names,
        'importance': model.feature_importances_
    }).sort_values('importance', ascending=False)

    print("\n📈 TOP 10 IMPORTANT FEATURES:")
    print(importance_df.head(10).to_string(index=False))

    # Save feature importance
    importance_df.to_csv('feature_importance.csv', index=False)

